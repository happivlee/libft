#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*tmp;

	if (n == 0)
		return ;
	i = 0;
	tmp = (char *)s;
	while (i < n)
	{
		*tmp = 0;
		tmp++;
		i++;
	}
}
